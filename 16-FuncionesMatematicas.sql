-- 16- Funciones matematicas
-- funcion abs: valor asoluto
select abs(-50) from dual;

-- funcion ceil: redondea un numero hacia arriba
select ceil(15.50) from dual;

-- funcion floor: redondea un numero hacia abajo
select floor(13.30) from dual;

-- funcion mod: devuelve el residuo de dos numeros enteros
select mod(10, 3) from dual;

-- funcion power: potencia
select power(10, 3) from dual;

-- funcion sqrt: raiz
select sqrt(27) from dual;